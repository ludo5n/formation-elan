<?php
SELECT b.NOM_BATAILLE,DATE_FORMAT(DATE_BATAILLE ,"%d/%m/%Y"),l.NOM_LIEU
FROM bataille b,lieu l 
WHERE b.ID_BATAILLE=l.ID_LIEU
ORDER BY DATE_BATAILLE DESC;
