<?php
SELECT s.NOM_SPECIALITE, COUNT(v.ID_VILLAGEOIS) AS nb_gaulois
FROM villageois v, specialite s
WHERE v.ID_SPECIALITE = s.ID_SPECIALITE
GROUP BY s.NOM_SPECIALITE
ORDER BY nb_gaulois DESC ;
?>
